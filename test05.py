#Operator ตัวดำเนินการ
# 1. Arithmetic Operator เครื่องคำนวณ
# (+, -, *, /, % mod, // floor, ** eponetial)
print(10 + 3)
print(10 - 3)
print(10 * 3)
print(10 / 3)
print(10 % 3) #หารเอาเศษ
print(10 // 3) #หารเอาส่วน
print(10 ** 3)
print('+++++++++++++++++++')
#2. Comparison Operator เครื่องหมายเปรียนเทียบ (==, !=, >, <, >=, <=)
#ผลลัพธ์ที่จะได้ True False
print(10 == 3)
print(10 != 3)
print('Bangna' >= 'Bangpoo')
print('Thailand' < 'THAILAND')
print('+++++++++++++++++++++++++')
# 3. Logical Operator เครื่องหมาย ตรรกะ not, and, or
# ข้อมูลที่จะใช้กับเครื่องหมายนี้จะต้องเป็น True/False และผลลัพธ์ที่จะได้จากใช้เครื่องหมาย True/False
print(not True)
print(not False)
print('----')
print(True and True)#True
print(True and False)
print(False and True)
print(False and False)
print('----')
print(True or True)#False
print(True or False)
print(False or True)
print(False or False)
# if-elif-else

score = 68

if score >= 80 : #colon อย่าลืม
    print('Grede A')
elif score >= 70 : #colon อย่าลืม
    print('Grede B')
elif score >= 60 : #colon อย่าลืม
    print('Grede C')
elif score >= 50 : #colon อย่าลืม
    print('Grede D')
else : #colon อย่าลืม *** else สุดท้ายจะเป็น eilf ก็ได้
    print('Grede F')
    
